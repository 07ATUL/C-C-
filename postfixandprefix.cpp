//Postfix and prefix operations
#include<iostream>
using namespace std;
int main()
{
	int a,b;
	b=7;
	a=b++;
	cout<<"a"<<a<<endl;
	cout<<"b"<<b<<endl;
//But now we have

    b=7;
    a=++b;
    cout<<"a"<<a<<endl;
	cout<<"b"<<b;
// Similary in the case of subraction prefix and postfix one
	
}
